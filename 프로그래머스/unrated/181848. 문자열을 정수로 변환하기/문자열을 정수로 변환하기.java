class Solution {
    public int solution(String n_str) {
        int answer = Integer.parseInt(n_str);   // 문자열은 참조형 자료형이기 때문에
                                                // - 같은 참조형 자료형인 Integer 자료형의 함수를 활용하여
                                                // - 해당 문자열을 정수로 변환
        return answer;
    }
}